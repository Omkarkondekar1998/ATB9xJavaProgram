package Oct.ex_10102024_constant_and_literal;

public class Lab034 {
    public static void main(String[] args) {
        int integerLiteral = 10;            // Integer literal
        double doubleLiteral = 3.14;        // Floating-point literal
        char charLiteral = 'A';              // Character literal
        String stringLiteral = "Hello";      // String literal
        boolean booleanLiteral = true;       // Boolean literal

        System.out.println(integerLiteral);
        System.out.println(doubleLiteral);
        System.out.println(charLiteral);
        System.out.println(stringLiteral);
        System.out.println(booleanLiteral);


    }
}
