package Oct.ex_21102024;

public class Lab102 {

    // to find even numbers 0 to 50.

    public static void main(String[] args) {

        for(int i = 0; i<=50; i++)
        {
            if(i%2==0)

            {
                System.out.println("Even");
            }
            else
            {
                System.out.println(i);
            }

        }
    }



}
