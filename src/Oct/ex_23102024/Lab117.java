package Oct.ex_23102024;

public class Lab117 {

    // Standard main method - entry point of the program
    public static void main(String[] args) {
        // This is where the program starts executing.
    }

    // Overloaded main method with an int parameter
    public static void main(int a) {
        // This method can be called with an integer argument.
    }

    // Overloaded main method with a single String parameter
    public static void main(String args) {
        // This method can be called with a single string argument.
    }

    // Overloaded main method returning an int
    public static int main() {
        // This method can be called without any arguments and returns an integer.
        return 18;
    }

    // Overloaded main method returning a String
    public static String main(String a, String b) {
        // This method takes two strings and returns their concatenation.
        return a + b;
    }
}