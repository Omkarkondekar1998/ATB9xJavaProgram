package Oct.ex_09102024;

public class Lab018 {

    public static void main(String[] args) {

        byte b = 100;  //8 bits, 1 Byte
        short s = 10000;  //16 bits, 2 Byte
        int i = 100000;  //32 bits, 4 Byte
        long l = 100000L;  //64bits, 8 Byte
        float f = 10.5f;  //32 bits, 4 Byte
        double d = 10.5;  //64 bits 8 Byte
        char c = 'A'; //16 bits 2 Byte
        boolean isTrue = true;  //64 Bits 8 Byte



        System.out.println("Byte: " + b);
        System.out.println("Short: " + s);
        System.out.println("Int: " + i);
        System.out.println("Long: " + l);
        System.out.println("Float: " + f);
        System.out.println("Double: " + d);
        System.out.println("Char: " + c);
        System.out.println("Boolean: " + isTrue);

    }
}
